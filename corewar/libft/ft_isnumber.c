#include "libft.h"

int			ft_isnumber(const char *s)
{
	int		i;

	i = -1;
	if (!s)
		return (0);
	while (s[++i] != '\0')
	{
		if (!ft_isdigit(s[i]))
			return (0);
	}
	return (1);
}
