#include "libft.h"

void	ft_putlnbr(long long n)
{
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			n = -n;
			ft_putchar('-');
		}
		if (n >= 10)
		{
			ft_putlnbr(n / 10);
			ft_putlnbr(n % 10);
		}
		else
			ft_putchar(n + 48);
	}
}
