#include "libft.h"

int		ft_count_nbr(char *str)
{
	int		i;
	int		k;

	i = 0;
	k = 0;
	while (str[i] != '\0')
	{
		while ((str[i] >= '0' && str[i] <= '9') || str[i] == '-')
		{
			i++;
			if ((str[i] < '0' || str[i] > '9') && str[i] != '-')
				k++;
		}
		if (str[i] != '\0')
			i++;
	}
	return (k);
}
